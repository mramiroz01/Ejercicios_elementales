
package com.tiernoparla.ejercicios_elementales;

public class main {
    public static void main(String[] args) {
        int i;
        
        i = 0;
        while (i <= 10)
        {
            if (i % 2 == 0)
                System.out.println(i);
            i++;
        }
        System.out.println("########################");
        for (int n = 0; n <= 10; n++)
        {
            if (n % 2 == 0)
                System.out.println(n);
        }
    }
    
}
